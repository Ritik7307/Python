# list is a ordered collection of datatypes. They are mutable
list = [1, 2.5, [-4,5], "hello", False]
print(list)
print(type(list))

# tuple is an ordered collection of datatypes which is immutable
tuple = (1, 2.5, [-4,5], "hello", False)
print(tuple)

# set is an unordered collection of unique datatypes. They are mutable
set = {1, 2.5, -4, "hello", False}
print(set)
print(type(set))

# dictionary is an unordered collection of key-value pairs. They are mutable
dict = {"name": "Alice", "age": 30, "is_student": False}
print(dict)

